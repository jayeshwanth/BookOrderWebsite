The author recommends this book for self study, or for homework assignments for a class. Download the author's <a href="http://www.byinquisition.org/uploads/610_syllabus.pdf" target="_blank">syllabus</a> for using this book as part of a first semester graduate course on organic reactions. 

<br />
<br />
In that syllabus, the author recommends the textbook by <a href="http://www.oup.com/uk/orc/bin/9780198503460/" target="_blank">Clayden, Greeves, Warren, and Wothers "Organic Chemistry"</a> to provide background for many of the problems. 
<br />
<br />
Students may find these videos useful, on <a href="https://youtu.be/XmdMnSoJ7Vg" target="_blank">fundamentals of additon of anions to carbonyls</a>, <a href="https://youtu.be/det_Tng0qTo" target="_blank">fundamentals of oxidation states</a>, <a href="https://youtu.be/cEUkRE941t8" target="_blank">some organic chemistry of organoboranes (may not play outside US)</a>, <a href="https://youtu.be/1pOUEFEdhGA" target="_blank">oxidation reactions</a>, and <a href="https://youtu.be/L_ug1PuSb_A" target="_blank">asymmetric reduction reactions</a>.
